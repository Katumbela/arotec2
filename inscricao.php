        function updateTimer() {
          future = Date.parse("jun 23, 2022 11:00:00");
          now = new Date();
          diff = future - now;

          days = Math.floor(diff / (1000 * 60 * 60 * 24));
          hours = Math.floor(diff / (1000 * 60 * 60));
          mins = Math.floor(diff / (1000 * 60));
          secs = Math.floor(diff / 1000);

          d = days;
          h = hours - days * 24;
          m = mins - hours * 60;
          s = secs - mins * 60;

          document.getElementById("timer")
            .innerHTML =
            '<div class="col-md-2">' + d + '<span style="margin: 0 auto !important;">Dias</span></div>' +
            '<div class="col-md-2">' + h + '<span style="margin: 0 auto !important;">Horas</span></div>' +
            '<div class="col-md-2">' + m + '<span style="margin: 0 auto !important;">Minutos</span></div>' +
            '<div class="col-md-2">' + s + '<span style="margin: 0 auto !important;">Segundos</span></div>';
        }
        setInterval('updateTimer()', 1000);
